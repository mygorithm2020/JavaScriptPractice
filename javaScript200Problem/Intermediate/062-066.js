// isNaN : Not a Number(숫자가 아님)
console.log(Number.isNaN(NaN));


// isInteger : 정수 확인
console.log(Number.isInteger(0));


// isArray: 배열 확인
Array.isArray("sdsds");

// parseInt : 문자열을 숫자형 정수로 변환
console.log(parseInt("1212"), 10);

// parseFloat : 실수형 숫자로 변환
console.log(parseFloat(5.5));
console.log(parseFloat("5.5"));
console.log(parseFloat("5.5두구두구구"));

